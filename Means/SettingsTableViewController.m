- (IBAction)composeMail {
    if ([MFMailComposeViewController canSendMail]) {
        MFMailComposeViewController *mfViewController = [[MFMailComposeViewController alloc] init];
        mfViewController.mailComposeDelegate = self;
        
        /*
        NSArray *toRecipients = [NSArray arrayWithObjects:@"help@example.com", @"secondMail@example.com", nil];
        */
        NSArray *toRecipients = [NSArray arrayWithObjects:@"help@worbb.com", nil];
        [mfViewController setToRecipients:toRecipients];
        [mfViewController setSubject:@"Hello."];
        
        [self presentModalViewController:mfViewController animated:YES];        
    }else {
        UIAlertView *alert = [[UIAlertView alloc] initWithTitle:@"Status:" message:@"iPhone is not configured to support Mail." delegate:nil cancelButtonTitle:@"OK" otherButtonTitles:nil];
        
        [alert show];
    }
    
}

- (void)mailComposeController:(MFMailComposeViewController*)controller didFinishWithResult:(MFMailComposeResult)result error:(NSError*)error {
    UIAlertView *alert = [[UIAlertView alloc] initWithTitle:@"Status:" message:@"" delegate:nil cancelButtonTitle:@"OK" otherButtonTitles:nil];
    
    switch (result) {
        case MFMailComposeResultCancelled:
            alert.message = @"Message Canceled";
            break;
        case MFMailComposeResultSaved:
            alert.message = @"Message Saved";
            break;
        case MFMailComposeResultSent:
            alert.message = @"Message Sent";
            break;
        case MFMailComposeResultFailed:
            alert.message = @"Message Failed";
            break;
        default:
            alert.message = @"Message Not Sent";
            break;
    }
    
    [self dismissModalViewControllerAnimated:YES];
    
    //[alert show];
}

